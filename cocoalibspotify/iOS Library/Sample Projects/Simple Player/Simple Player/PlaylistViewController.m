        playlistTabBarItem.title = @"Playlist";
        UIImage *tabBarImage = [UIImage imageNamed:@"text-list.png"];
        playlistTabBarItem.image = tabBarImage;
        self.tabBarItem = playlistTabBarItem;
    }
    return self;
}

